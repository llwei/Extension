import UIKit

extension Int {
    
    var toFloat: Float {
        return Float(self)
    }
    
    var toCGFloat: CGFloat {
        return CGFloat(self)
    }
    
    var toDouble: Double {
        return Double(self)
    }
    
    var toInt64: Int64 {
        return Int64(self)
    }
    
    var toUInt64: UInt64 {
        return UInt64(self)
    }
    
}
